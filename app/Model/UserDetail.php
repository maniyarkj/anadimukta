    public function beforeValidate($options = array())
    {
        foreach ($this->_dateFields as $field) {
            if (!empty($this->data[$this->alias][$field])) {
                $this->data[$this->alias][$field] = $this->toSqlDateFormat(
                        $this->data[$this->alias][$field]
                );
            }
        }

        return true;
    }

    public function afterFind($results, $primary = false)
    {
        $results = parent::afterFind($results, $primary);

        foreach ($results as $idx => $result) {
            if (!isset($result['UserDetail'])) {
                continue;
            }
            // Change date fields to readable
            foreach ($this->_dateFields as $field) {
                if (!empty($result['UserDetail'][$field])) {
                    $results[$idx]['UserDetail']['sql_'.$field] = $results[$idx]['UserDetail'][$field];
                    $results[$idx]['UserDetail'][$field] = $this->toDateReadable(
                            $result['UserDetail'][$field]
                    );
                }
            }
            $results[$idx]['UserDetail']['photo_url'] = '';
            if ($result['UserDetail']['photo']) {
                $results[$idx]['UserDetail']['photo_url'] = '/files/user_detail/photo/'
                        . $result['UserDetail']['id']
                        . '/'
                        . $result['UserDetail']['photo'];
            }
        }
        return $results;
    }
